package com.board.games.handler.generic;

import com.cubeia.firebase.api.action.local.LoginRequestAction;
import com.cubeia.firebase.api.action.local.LoginResponseAction;
import com.cubeia.firebase.api.login.LoginHandler;

public class GenericLoginHandler implements LoginHandler {

	@Override
	public LoginResponseAction handle(LoginRequestAction req) {
		LoginResponseAction response = null;
//		System.out.println("***** INSIDE GENERID ****");
		response = new LoginResponseAction(true, req.getUser(),
				Integer.parseInt(req.getPassword())); // pid.incrementAndGet()
		return response;
	}

}
